def sockMerchant(n, ar):
    # Write your code here
    hashmap = {}
    
    for element in ar:
        if element not in hashmap:
            hashmap[element] = 1
            continue
        hashmap[element] += 1
    
    result = 0
    for element in hashmap.values():
        result += int(element/2)

    return result

